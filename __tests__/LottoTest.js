import Lotto from "../src/Lotto";

describe("로또 클래스 테스트", () => {
  test("로또 번호의 개수가 6개가 아니면 예외가 발생한다.", () => {
    expect(() => {
      new Lotto([1, 2, 3, 4, 5, 6, 7]); // 7개일 경우 예외 발생
    }).toThrow("[ERROR] 로또 번호는 6개여야 합니다.");

    expect(() => {
      new Lotto([1, 2, 3, 4, 5]); // 5개일 경우 예외 발생
    }).toThrow("[ERROR] 로또 번호는 6개여야 합니다.");
  });

  // TODO: 테스트가 통과하도록 프로덕션 코드 구현
  test("로또 번호에 중복된 숫자가 있으면 예외가 발생한다.", () => {
    expect(() => {
      new Lotto([1, 2, 3, 4, 5, 5]); // 중복된 숫자 5
    }).toThrow("[ERROR] 로또 번호에는 중복이 없어야 합니다.");

    expect(() => {
      new Lotto([1, 1, 2, 3, 4, 5]); // 중복된 숫자 1
    }).toThrow("[ERROR] 로또 번호에는 중복이 없어야 합니다.");
  });

  // TODO: 추가 기능 구현에 따른 테스트 코드 작성
});
